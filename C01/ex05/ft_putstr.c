#include<unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

int main()
{
	char a[10] = "heybecch";

	ft_putstr("heeeybeech");
	return 0;

}
