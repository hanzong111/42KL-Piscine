char	*ft_strlowcase(char *str)
{
	char	*init_ptr;

	init_ptr = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
			*str = *str + 32;
		str++;
	}
	*str = '\0';
	return (init_ptr);
}
