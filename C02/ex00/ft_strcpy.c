char	*ft_strcpy(char *dest, char *src)
{
	char	*init_dest;

	init_dest = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (init_dest);
}
