char	*ft_strupcase(char *str)
{
	char	*init_ptr;

	init_ptr = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
			*str = *str - 32;
		str++;
	}
	*str = '\0';
	return (init_ptr);
}
